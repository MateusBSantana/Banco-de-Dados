CREATE OR REPLACE VIEW actual_database_objects_view AS
SELECT 'TABLE' AS OBJECT_TYPE, COUNT(*) AS ACTUAL_COUNT
FROM information_schema.TABLES
WHERE TABLE_SCHEMA = 'sqlfun'
UNION
SELECT 'VIEW', COUNT(*)
FROM information_schema.VIEWS
WHERE TABLE_SCHEMA = 'sqlfun'
UNION
SELECT 'ROUTINE', COUNT(*)
FROM information_schema.ROUTINES
WHERE ROUTINE_SCHEMA = 'sqlfun';

-- VERIFICAR
CREATE OR REPLACE VIEW actual_constraints_view AS
SELECT CASE 
           WHEN CONSTRAINT_TYPE = 'PRIMARY KEY' THEN 'PRIMARY KEY CONSTRAINT'
           WHEN CONSTRAINT_TYPE = 'FOREIGN KEY' THEN 'REFERENTIAL INTEGRITY CONSTRAINT'
           WHEN CONSTRAINT_TYPE = 'UNIQUE' THEN 'UNIQUENESS CONSTRAINT'
           WHEN CONSTRAINT_TYPE = 'CHECK' THEN 'CHECK CONSTRAINT'
           ELSE CONSTRAINT_TYPE
       END AS CONSTRAINT_TYPE,			
       COUNT(*) AS ACTUAL_COUNT
FROM information_schema.TABLE_CONSTRAINTS
WHERE TABLE_SCHEMA = 'sqlfun'
GROUP BY CONSTRAINT_TYPE;


