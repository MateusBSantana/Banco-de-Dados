-- CREATE THE numeros_0_a_99 VIEW
CREATE OR REPLACE VIEW numeros_0_a_99 AS
SELECT (A.digito * 10) + B.digito AS N
FROM numeros_0_a_9 A,
     numeros_0_a_9 B;

-- CREATE THE numeros_0_a_99999 VIEW
CREATE OR REPLACE VIEW numeros_0_a_99999 AS
SELECT ((A.digito * 10000) + (B.digito * 1000) + (C.digito * 100) + (D.digito * 10) + E.digito) AS N
FROM numeros_0_a_9 A,
     numeros_0_a_9 B,
     numeros_0_a_9 C,     
     numeros_0_a_9 D,
     numeros_0_a_9 E;

CREATE OR REPLACE VIEW NUMBERS_1_TO_1000 AS
SELECT ((A.digito * 100) + (B.digito * 10) + C.digito + 1) AS N
FROM numeros_0_a_9 A,
     numeros_0_a_9 B,
     numeros_0_a_9 C;     
     

